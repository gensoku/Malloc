#include "ft_malloc.h"

void		ma_print_hex(int i)
{
	static char	arr[16] = "0123456789abcdef";

	if (i > 16)
	{
		ma_putchar(arr[i % 16]);
		i /= 16;
	}
	ma_putchar(arr[i]);
}
