#include "ft_malloc.h"

void	ma_putnbr(int n)
{
	if (n == -2147483648)
	{
		ma_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ma_putchar('-');
		n = -n;
	}
	if (n > 9)
	{
		ma_putnbr(n / 10);
		ma_putnbr(n % 10);
	}
	else
		ma_putchar(n + 48);
}
